namespace InvvardDev.EZLayoutDisplay.PluginContract.Enum
{
    /// <summary>
    /// Enum that indicates the direction to switch to.
    /// </summary>
    public enum SwitchDirection
    {

        /// <summary>
        /// Switch direction is going down.
        /// </summary>
        Down = -1,

        /// <summary>
        /// No switch direction applied.
        /// </summary>
        Stay = 0,

        /// <summary>
        /// Switch direction is going up.
        /// </summary>
        Up = 1,
    }
}